    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            String a1 = auser.getText();
            String a2 = apass.getText();
            Class.forName("com.mysql.jdbc.Driver");
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/gasreservation","root","");
            Statement s = c.createStatement();
            String sql = "select * from Admin where User='"+a1+"' and Password='"+a2+"'";
            
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
               JOptionPane.showMessageDialog(rootPane, "Login Successfully!");
               AdminOption ao = new AdminOption();
               ao.setVisible(true);
            }else {
                JOptionPane.showMessageDialog(rootPane, "Wrong UserName or Password!");
                auser.setText("");
                apass.setText("");
            };
                  
            
        }catch (Exception e){
            System.out.println(e.getMessage());
        };
    }//GEN-LAST:event_jButton1ActionPerformed

